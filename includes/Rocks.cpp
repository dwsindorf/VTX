//-------------------------------------------------------------
// TNrocks::applyExpr() apply expr value
//-------------------------------------------------------------
void TNrocks::applyExpr()
{
	if(!expr)
		return;
 	TNrocks* rocks=(TNrocks*)expr;
 	if(rocks->typeValue()!=typeValue()){
    	NodeIF *p=getParent();
  		rocks->base=base;
 		if(base)
 			base->setParent(rocks);
 		rocks->setParent(p);
		rocks->right=right;
 		if(right)
 			right->setParent(rocks);
  		p->replaceChild(this,rocks);
		rocks->init();
 		right=0;
 		base=0;
 		expr=0;
 		DFREE(left);
 	}
	else{ // args changed only
		DFREE(left);
		left=rocks->left;
		left->setParent(this);
		rocks->init();
		delete mgr;
		type=rocks->type;
		mgr=rocks->mgr;
		rocks->left=0;
		rocks->mgr=0;
		rocks->base=0;
		eval();
		delete rocks;
		expr=0;
	}
 }

