#define ROCK_LAYER
//-------------------------------------------------------------
// TNrocks::eval() evaluate the node
//-------------------------------------------------------------
void TNrocks::eval() {
	if (!isEnabled() || TheScene->viewtype != SURFACE) {
		if (right)
			right->eval();
		return;
	}

	TerrainData rock;
	TerrainData ground;
	int i;
	bool first = (right && right->typeValue() != ID_ROCKS);
	bool last = getParent()->typeValue() != ID_ROCKS;
	INIT;
#ifdef ROCK_LAYER
	S0.set_flag(ROCKLAYER);
#endif
	int in_map = S0.get_flag(CLRTEXS);

	if (CurrentScope->rpass()) {
		INIT;
		if(right) // ground
			right->eval();
		INIT;
#ifdef ROCK_LAYER
		Td.add_id();
		Td.tp->set_rock(true);
		Td.tp->ntexs=0;
		Td.tp->set_rock(true);
		if(!in_map)
			S0.set_flag(CLRTEXS);
		if(base)// rock texs
			base->eval();
		if(!in_map)// in case we were in another map on entry
			S0.clr_flag(CLRTEXS);
#else
		if(base)// rock texs
			base->eval();
#endif
		mgr->setHashcode();
		return;
	}
#ifdef ROCK_LAYER
	if (!in_map && first)
		Td.begin();
#endif
	ground.p.z = 0;

	INIT;
	bool other_rock = false;
	if (right)
		right->eval(); // ground
	other_rock = S0.get_flag(ROCKBODY);
	S0.p.z-=Drop;
	if (first) {
		S0.next_id();
		Td.insert_strata(S0);
	}
	ground.copy(S0);
	INIT;

	RockMgr *rmgr = (RockMgr*) mgr;
	
	mgr->getArgs((TNarg *)left);
	INIT;
	if (base)
		base->eval();
	S0.p.z=ground.p.z;
	rmgr->base = S0.p.z-rmgr->drop*rmgr->maxsize/Hscale;
#ifdef ROCK_LAYER
	S0.next_id();
#endif
	rock.copy(S0);
	INIT;
	rmgr->ht = mgr->base;
	rmgr->eval();  // calls set_terrain sets mgr->ht

	if (rmgr->noise_amp)
		CurrentScope->revaluate();
	rock.p.z = rmgr->ht;
	double delta = (rock.p.z - ground.p.z) / fabs(ground.p.z);
	if (delta > 0) {
		rock.p.x = rmgr->rx * (1 - rmgr->rdist);
		rock.p.y = rmgr->ry * (1 - rmgr->rdist);
		S0.copy(rock);
#ifdef ROCK_LAYER
		Td.tp->set_rock(true);
		S0.set_flag(ROCKBODY);
#endif
	}
	else {
		Td.tp->set_rock(false);
		S0.copy(ground);
#ifdef ROCK_LAYER
		if(!other_rock)
			S0.clr_flag(ROCKBODY);
#endif
	}
#ifdef ROCK_LAYER
	Td.insert_strata(rock);
	if (!in_map && last)
		Td.end();
#endif
}

