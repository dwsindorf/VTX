//-------------------------------------------------------------
// TNplant::eval() evaluate the node
//-------------------------------------------------------------
void TNplant::eval()
{	
	
	PlantMgr *smgr=(PlantMgr*)mgr;

	TerrainData ground;
	int hits;
	double cval;

	if(!isEnabled() || TheScene->viewtype !=SURFACE || Raster.surface==2){
		if(right)
			right->eval();
		return;
	}
	SINIT;
	if(CurrentScope->rpass()){
		int instance=0;
		int layer=0;
		bool inlayer=inLayer();
		if(inlayer){
			instance=Td.tp->plants.size;
			layer=Td.tp->type();
		}
		else
			instance=Td.plants.size;
		mgr->instance=instance;
		mgr->layer=layer;
		if(plant){
			plant->set_id(instance);
			plant->layer=layer;
		}
		if(inlayer)
			Td.tp->add_plant(plant);
		else
			Td.add_plant(plant);
		mgr->setHashcode();
		if(right)
			right->eval();
		return;
	}
	if(!CurrentScope->spass()){ // surface generation
		if(right)
			right->eval(); // get ht 
		if(!smgr->test())  // no modulation needed
		    return;
		// modify density and/or color of surface nodes
		Height=S0.p.z;
		MapPt=TheMap->point(Theta,Phi,Height)-TheScene->xpoint;
		smgr->htval=Height;		
		ground.copy(S0); // save surface data
	}
    extern double Hardness;
	SINIT;
	Color c =Color(1,1,1);
	
	ncalls++;
	
	INIT;
	
	double b,f,h;
	double density=1;
	MaxSize=mgr->maxsize;
	radius=PSCALE;
	
	mgr->type=type;
	if(smgr->slope_bias){
		b=smgr->slope_bias;
		h=4*zslope();
		f=8*b * (h - 0.25);
		f=clamp(f,-1,1);
		density+=fabs(b)*f;
#ifdef DEBUG_SLOPE_BIAS
		if(ncalls%100==0)
			cout<<"bias:"<<b<<" slope:"<<h<<" f:"<<f<<" d:"<<density<<endl;
#endif
	}
	if(smgr->ht_bias){
		h=Height;
		b=smgr->ht_bias;
		f=b * (h - 0.5) - 0.5;
		f=clamp(f,-1,1);
		//f=2*lerp(8*fabs(smgr->ht_bias)*Height,-1,1,-smgr->ht_bias,smgr->ht_bias);
		density+=fabs(b)*f;
#ifdef DEBUG_HT_BIAS
		if(ncalls%100==0)
			cout<<"bias:"<<b<<" ht:"<<h<<" f:"<<f<<" d:"<<density<<endl;
#endif
	}
	if(smgr->lat_bias){
		h=fabs(2*Phi/180);
		b=smgr->lat_bias;
		f=2*b * (h - 0.5);
		f=clamp(f,-1,1);
		density+=fabs(b)*f;
#ifdef DEBUG_LAT_BIAS
		if(ncalls%100==0)
			cout<<"bias:"<<b<<" h:"<<h<<" f:"<<f<<" d:"<<density<<endl;
#endif
	}
	if(smgr->hardness_bias){
		b=smgr->hardness_bias;
		h=Hardness;
		f=b * (h - 0.5) - 0.5;
		f=clamp(f,-1,1);
		density+=fabs(b)*f;
#ifdef DEBUG_HARD_BIAS
		static double lasth=0;
		if(ncalls%100==0 && lasth !=hardness){
			cout<<"bias:"<<b<<" hardness:"<<h<<" f:"<<f<<" d:"<<density<<endl;
			lasth=hardness;
		}
#endif
	}
    density*=maxdensity;
	density=clamp(density,0,1);
	mgr->density=density;
	if(density<=0)
		return;
	smgr->eval();  // calls PlantPoint.set_terrain (need MapPt)
	
	if(!CurrentScope->spass()){ // adapt pass (else render-plant creation pass)
		S0.copy(ground); //restore surface data
		smgr->setTests();
	}
}

