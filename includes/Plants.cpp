NodeIF *TNBranch::replaceNode(NodeIF *c){
	if(!c || c->typeValue()!=typeValue())
		return 0;
	if(left)
		delete left;
	if(base)
		delete base;
	TNBranch *newbranch=(TNBranch *)c;
	left=newbranch->left;
	left->setParent(this);
	base=newbranch->base;
	setName(newbranch->nodeName());
	if(base)
		base->setParent(this);
	init();
	return this;
}

